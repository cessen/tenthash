#include <stdio.h>
#include "tenthash.h"

void main() {
    const static uint8_t inputs[][64] = {
        {},
        {0},
        "0123456789",
        "abcdefghijklmnopqrstuvwxyz",
        "The quick brown fox jumps over the lazy dog.",
    };
    const static int inputs_len[] = {0, 1, 10, 26, 44};
    const static uint8_t digests[][24] = {
        {0x71, 0xa1, 0x25, 0x21, 0xe9, 0x27, 0x61, 0x73, 0x53, 0xca, 0xdc, 0xbb, 0x22, 0x47, 0x53, 0x22, 0x03, 0x6e, 0x57, 0x52},
        {0x3e, 0x45, 0xee, 0x52, 0x42, 0x2a, 0xa2, 0xf9, 0x35, 0xc7, 0x0a, 0x22, 0x06, 0xb4, 0x3a, 0xfc, 0x92, 0xdc, 0xe3, 0x37},
        {0x9c, 0x21, 0xdd, 0xc6, 0x5d, 0xcc, 0x3a, 0xd2, 0x98, 0x2c, 0x32, 0x1c, 0x71, 0x74, 0xc4, 0x75, 0xdc, 0x70, 0x5e, 0x03},
        {0xbf, 0x1d, 0x10, 0x62, 0x7b, 0x6f, 0x80, 0x94, 0xe3, 0x33, 0x11, 0x1e, 0x64, 0x6a, 0x37, 0xf7, 0xed, 0xcf, 0xab, 0xb6},
        {0x21, 0x22, 0x7d, 0x4c, 0xb3, 0xe7, 0x52, 0xb0, 0x5a, 0x38, 0x6d, 0x68, 0x58, 0x06, 0x72, 0xec, 0x8f, 0x26, 0xd6, 0x77},
    };

    int failures = 0;
    for (int i = 0; i < 5; i++) {
        Digest output = hash(inputs[i], inputs_len[i]);

        for (int j = 0; j < 20; j++) {
            if (output.bytes[j] != digests[i][j]) {
                printf("Failed test vector %d.\n", i + 1);
                failures++;
                break;
            }
        }
    }

    if (failures == 0) {
        printf("All test vectors passed.\n");
    }
}
