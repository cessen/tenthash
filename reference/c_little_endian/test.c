#include <stdio.h>
#include "tenthash.h"

void main() {
    const static uint8_t inputs[][64] = {
        {},
        {0},
        "0123456789",
        "abcdefghijklmnopqrstuvwxyz",
        "The quick brown fox jumps over the lazy dog.",
    };
    const static int inputs_len[] = {0, 1, 10, 26, 44};
    const static uint8_t digests[][24] = {
        {0xe0, 0xf5, 0x79, 0xd7, 0x7b, 0x71, 0xae, 0x9a, 0x5a, 0xac, 0xf6, 0x76, 0x42, 0xf4, 0x2a, 0x6f, 0x6d, 0x8b, 0x57, 0xe4},
        {0x6c, 0x3c, 0xc3, 0xb2, 0xda, 0x66, 0x3b, 0xaf, 0x11, 0x3d, 0xe2, 0xe8, 0xcb, 0xd9, 0x23, 0x16, 0x3a, 0x87, 0x84, 0x7a},
        {0x4b, 0x49, 0xac, 0xd5, 0x9a, 0x54, 0x82, 0xf3, 0x9a, 0x0e, 0x7f, 0x89, 0x35, 0x14, 0x9d, 0xf9, 0x4e, 0xe6, 0x91, 0x85},
        {0x66, 0xa2, 0xbd, 0x21, 0x0f, 0x2c, 0xdf, 0x08, 0xaa, 0xb5, 0xfe, 0x66, 0x27, 0x48, 0x4c, 0x15, 0x7c, 0x7c, 0x98, 0xb9},
        {0x71, 0x5c, 0xd5, 0x73, 0x97, 0x68, 0x59, 0x66, 0x3c, 0x2e, 0xba, 0xf2, 0x1d, 0xac, 0xd7, 0xbd, 0x78, 0xe1, 0x1a, 0x71},
    };

    int failures = 0;
    for (int i = 0; i < 5; i++) {
        Digest output = hash(inputs[i], inputs_len[i]);

        for (int j = 0; j < 20; j++) {
            if (output.bytes[j] != digests[i][j]) {
                printf("Failed test vector %d.\n", i + 1);
                failures++;
                break;
            }
        }
    }

    if (failures == 0) {
        printf("All test vectors passed.\n");
    }
}
